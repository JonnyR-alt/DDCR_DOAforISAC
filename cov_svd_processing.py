    parser.add_argument("--M", type=int, default=M)
    parser.add_argument("--T", type=int, default=T)
    parser.add_argument("--snr", type=float, default=snr)
    parser.add_argument("--signal_type", type=str, default=signal_type)
    parser.add_argument("--signal_nature", type=str, default=signal_nature)
    parser.add_argument("--eta", type=float, default=eta)
    parser.add_argument("--bias", type=float, default=bias)
    parser.add_argument("--sv_noise_var", type=float, default=sv_noise_var)
    parser.add_argument("--doa_gap", type=float, default=10.0)
    if fixed_gap:
        parser.add_argument("--fixed_gap", action="store_true", default=True, help="Using exact given gap distance")
    else:
        parser.add_argument("--fixed_gap", action="store_true", help="Using exact given gap distance")
    parser.add_argument("--rank", type=int, default=rank, help="Truncation rank; default to M")
    parser.add_argument("--max_samples", type=int, default=None, help="Optional cap on number of snapshots per set before averaging")
