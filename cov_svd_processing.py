"""
Process covariance matrices generated by `generate_snapshots.py` / `f_gendata.py`:
- Support covariance npy with shape (T, N, N) *or* (num_sets, T, N, N), complex128.
- Mean over snapshot dimension T -> sample covariance of shape (num_sets, N, N).
- H 由含噪协方差均值计算（保持原逻辑）；U/S/Vh 改为无噪协方差均值的 SVD 结果。
- Save truncated U/S/Vh，以及按 r=0..rank_dim-1 依次“置零前 r 个最大奇异值”重构得到的 H，形状 (num_sets, rank_dim, N, N, 2)，
    文件名沿用 `set_dataset_filename` 后缀（不含 num_sets）。
"""

