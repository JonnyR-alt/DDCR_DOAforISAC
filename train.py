            found = None
            for s in cand:
                probe = "datasets/processed_cov_svd/" + f"iter_cov_noisy_step{s}.npy"
                if os.path.exists(probe):
                    found = s
                    break
            if found is not None and found != suffix:
                suffix = found
                print(f"[Suffix] Resolved dataset suffix by existing files: {suffix}")

