        if args_data.lambda_doa > 0:
            if doa_gt_phase1 is None:
                raise FileNotFoundError(f"lambda_doa>0 but DoA file not found: {doa_file}")
            if str(getattr(args_data, "doa_input", "pred_u")).lower() == "steering_u":
                if int(args_data.M) > 1:
                    raise ValueError("doa_input=steering_u is only supported for single-source training")
                if steering_u_phase1 is None:
                    raise ValueError("doa_input=steering_u but steering_u_phase1 is None")
