def _flat_grad_fill_none(grads, params):
    vec = []
    for g, p in zip(grads, params):
        if g is None:
            vec.append(torch.zeros_like(p).reshape(-1))
        else:
            vec.append(g.reshape(-1))
    return torch.cat(vec)

def _get_shared_params(model, scope="backbone"):
    # 你想验证“共享部分”是否冲突：通常选 backbone + u_head
    if scope == "all":
        return [p for p in model.parameters() if p.requires_grad]
    # backbone: freq_gate, input_proj, blocks, norm, u_head（不含 doa_u_*）
    names = ["freq_gate", "input_proj", "blocks", "norm", "u_head"]
    params = []
    for n in names:
        m = getattr(model, n, None)
        if m is None:
            continue
        params += [p for p in m.parameters() if p.requires_grad]
    return params


