    folder_path = f"datasets/weights/model{suffix}"
    if not os.path.exists(folder_path):
        os.makedirs(folder_path)

    # direct multi-source: output r=M vectors per sample
    model = DDCRNet(M=args_data.M, N=args_data.N, r=args_data.rank, dim=128, depth=4, attn_dim=64, k_len=args_data.N,refine_mode="both").to(device)

