    H_tensor_file = "datasets/processed_cov_svd/" + f"iter_cov_noisy_step{suffix}.npy"
    H_clean_file = "datasets/processed_cov_svd/" + f"iter_cov_clean_step{suffix}.npy"
    U_target_file = "datasets/processed_cov_svd/" + f"iter_u_target{suffix}.npy"
    S_target_file = "datasets/processed_cov_svd/" + f"svd_S{suffix}.npy"
