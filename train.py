def _flat_grad_fill_none(grads, params):
    vec = []
    for g, p in zip(grads, params):
        if g is None:
            vec.append(torch.zeros_like(p).reshape(-1))
        else:
            vec.append(g.reshape(-1))
    return torch.cat(vec)

def _get_shared_params(model, scope="backbone"):
    # Gradient diagnostics usually inspect the shared representation path.
    if scope == "all":
        return [p for p in model.parameters() if p.requires_grad]
    # Backbone path before the task-specific DoA readout.
    names = ["freq_gate", "input_proj", "blocks", "norm", "u_head"]
    params = []
    for n in names:
        m = getattr(model, n, None)
        if m is None:
            continue
        params += [p for p in m.parameters() if p.requires_grad]
    return params


def _build_suffix(params: SystemModelParams, samples_size: int) -> str:
    """Suffix consistent with dataset naming (no .h5)."""

    return set_dataset_filename(params, samples_size).replace(".h5", "")


